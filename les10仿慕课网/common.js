// 下载app部分
$(function () {
    $("#js-app-load").on("mouseover", function () {
        $(".js-load-box").show();
        $("#nav").addClass("addZ-indexCode");
    });
});
$(function () {
    $("#js-app-load").on("mouseout", function () {
        $(".js-load-box").hide();
        $("#nav").removeClass("addZ-indexCode")
    });
});

